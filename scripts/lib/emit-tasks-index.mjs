const GENERATED_HEADER = '<!-- GENERATED by scripts/sync-ralph-state.mjs — do NOT edit by hand -->'

export function buildTasksIndex(snapshot) {
    if (!snapshot || !Array.isArray(snapshot.tasks)) {
        throw new Error('buildTasksIndex requires a Snapshot with tasks')
    }

    const lines = [
        GENERATED_HEADER,
        '',
        '# Ralph Tasks Index',
        '',
        `Generated: ${snapshot.generatedAt || 'unknown'}`,
        `Source commit: ${snapshot.generatedFromCommit || 'unknown'}`,
        '',
    ]

    for (const task of snapshot.tasks) {
        const ralph = task.ralph
        const taskId = String(task.id)
        lines.push(`## ${escapeMarkdown(taskId)}`)
        lines.push(`- Stage: ${escapeMarkdown(ralph?.stage ?? 'untracked')}`)
        lines.push(`- jobDir: ${formatValue(ralph?.artifacts?.jobDir)}`)
        lines.push(`- Last activity: ${escapeMarkdown(describeLastActivity(snapshot, task))}`)
        lines.push(`- Dashboard: [${escapeMarkdown(taskId)}](../plans/overview.html#cmd-${encodeURIComponent(taskId)})`)
        lines.push('')
    }

    return `${lines.join('\n').trimEnd()}\n`
}

function describeLastActivity(snapshot, task) {
    const candidates = []

    for (const run of snapshot.runs ?? []) {
        if (run.taskId === task.id && run.ranAt) {
            candidates.push({
                timestamp: run.ranAt,
                label: [run.ranAt, run.id ? `(${run.id})` : '', run.outcome ?? '', run.summary ? `- ${run.summary}` : '']
                    .filter(Boolean)
                    .join(' '),
            })
        }
    }

    if (task.ralph?.lastUpdatedAt) candidates.push({ timestamp: task.ralph.lastUpdatedAt, label: `${task.ralph.lastUpdatedAt} Ralph update` })
    if (task.lastTouchedAt) candidates.push({ timestamp: task.lastTouchedAt, label: `${task.lastTouchedAt} task touch` })

    const latest = candidates
        .filter((candidate) => Number.isFinite(Date.parse(candidate.timestamp)))
        .sort((a, b) => Date.parse(b.timestamp) - Date.parse(a.timestamp))[0]

    return latest?.label ?? 'n/a'
}

function formatValue(value) {
    if (!value) return 'n/a'
    return `\`${String(value).replace(/`/g, '\\`')}\``
}

function escapeMarkdown(value) {
    return String(value).replace(/([\\[\]])/g, '\\$1')
}
